/// Returns the lowest ACI index whose RGB exactly matches `(r, g, b)`.
#[must_use]
pub fn exact_aci(r: u8, g: u8, b: u8) -> Option<u8> {
    (1..=255).find(|&i| ACI_RGB[i as usize] == (r, g, b))
}

/// Returns the lowest nearest ACI index by Euclidean RGB distance.
#[must_use]
pub fn nearest_aci(r: u8, g: u8, b: u8) -> u8 {
    let mut best = 1u8;
    let mut best_d = u32::MAX;
    for i in 1u16..=255 {
        let (cr, cg, cb) = ACI_RGB[i as usize];
        let dr = i32::from(cr) - i32::from(r);
        let dg = i32::from(cg) - i32::from(g);
        let db = i32::from(cb) - i32::from(b);
        let d = (dr * dr + dg * dg + db * db) as u32;
        if d < best_d {
            best_d = d;
            best = i as u8;
        }
    }
    best
}

/// Packs `(r, g, b)` into a 24-bit group-420 value `0x00RRGGBB`.
#[must_use]
pub const fn true_color(r: u8, g: u8, b: u8) -> i64 {
    ((r as i64) << 16) | ((g as i64) << 8) | (b as i64)
}

// ===================== Import conversions =====================

/// Returns RGB for an ACI index. Index 0 returns the table placeholder.
#[must_use]
pub(crate) const fn aci_to_rgb(i: u8) -> (u8, u8, u8) {
    ACI_RGB[i as usize]
}

/// Unpacks the low 24 bits of a group-420 true-color value into `(r, g, b)`.
#[must_use]
pub(crate) const fn unpack_true_color(v: i64) -> (u8, u8, u8) {
    (
        ((v >> 16) & 0xFF) as u8,
        ((v >> 8) & 0xFF) as u8,
        (v & 0xFF) as u8,
    )
}

#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn exact_matches_canonical_indices() {
        assert_eq!(exact_aci(255, 0, 0), Some(1)); // Pure red.
        assert_eq!(exact_aci(255, 255, 0), Some(2)); // Yellow.
        assert_eq!(exact_aci(0, 255, 0), Some(3)); // Green.
        assert_eq!(exact_aci(0, 0, 255), Some(5)); // Blue.
        assert_eq!(exact_aci(255, 255, 255), Some(7)); // White uses the lowest matching index.
    }

    #[test]
    fn nearest_for_offbeat_gray() {
        // The nearest palette value to gray (100, 100, 100) is ACI 251.
        assert_eq!(nearest_aci(100, 100, 100), 251);
        // An exact match is also its own nearest color.
        assert_eq!(nearest_aci(255, 0, 0), 1);
    }

    #[test]
    fn true_color_packs_24_bits() {
        assert_eq!(true_color(255, 0, 0), 0x00FF_0000);
        assert_eq!(true_color(1, 2, 3), 0x0001_0203);
    }

    #[test]
    fn aci_to_rgb_matches_table_and_true_color_roundtrips() {
        assert_eq!(aci_to_rgb(1), (255, 0, 0)); // Red.
        assert_eq!(aci_to_rgb(3), (0, 255, 0)); // Green.
        assert_eq!(aci_to_rgb(7), (255, 255, 255)); // White.
        // Unpack is the inverse of true_color and ignores high bits.
        assert_eq!(unpack_true_color(true_color(10, 20, 30)), (10, 20, 30));
        assert_eq!(unpack_true_color(0xC2_00_00_FF), (0, 0, 255));
    }
}
